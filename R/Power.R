#' square a number
#'
#'Take in a numeric value and squares it
#'@param x A numeric input
#'@return The sqaure of the output
#'@example
#'@export
square<-function(x){
  return(x^2)
}

#' Cube a number
#' @param x a number to be cubed
#' @return The cube of the input
#' @export
cube <-function(x){
  return(x^3)
}
